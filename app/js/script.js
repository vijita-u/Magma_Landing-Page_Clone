function canvas(page, data) {
	const canvas = document.querySelector(`${page} canvas`);
	const context = canvas.getContext("2d");

	canvas.width = window.innerWidth;
	canvas.height = window.innerHeight;

	// Resizing
	window.addEventListener("resize", () => {
		canvas.width = window.innerWidth;
		canvas.height = window.innerHeight;
		render();
	});

	// Creating image Array
	const imageFiles = data.split("\n"); // Image array
	const frameCount = imageFiles.length; // Image count

	// Creating an array of image tags with resp. image files
	const images = [];
	const imageSequence = {
		frame: 0,
	};

	// Creating <img /> tags & adding it to the images array
	for (let i = 0; i < frameCount; i++) {
		const img = new Image();
		img.src = imageFiles[i];
		images.push(img);
	}

	// Tweening an object in gsap
	gsap.to(imageSequence, {
		frame: frameCount,
		snap: "frame",
		ease: "none",
		scrollTrigger: {
			scrub: 0.5,
			trigger: `${page}`,
			start: "top top",
			end: "250% top",
			scroller: "#main",
		},
		onUpdate: render,
	});

	images[0].onload = render;

	function render() {
		scaleImage(images[imageSequence.frame], context);
	}

	function scaleImage(image, context) {
		let canvas = context.canvas;
		let horizontalRatio = canvas.width / image.width;
		let verticalRatio = canvas.height / image.height;
		let ratio = Math.max(horizontalRatio, verticalRatio);
		let centerShift_x = (canvas.width - image.width * ratio) / 2;
		let centerShift_y = (canvas.height - image.height * ratio) / 2;
		context.clearRect(0, 0, canvas.width, canvas.height);
		context.drawImage(
			image,
			0,
			0,
			image.width,
			image.height,
			centerShift_x,
			centerShift_y,
			image.width * ratio,
			image.height * ratio
		);
	}

	ScrollTrigger.create({
		trigger: `${page}`,
		pin: true,
		scroller: "#main",
		start: "top top",
		end: "250% top",
	});
}

