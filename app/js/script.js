function canvas_roadmap(page, data) {
	const canvas = document.querySelector(`${page} canvas`);
	const context = canvas.getContext("2d");

	const imageFiles = data.split("\n");
	const frameCount = imageFiles.length;

	const images = [];
	const imageSequence = {
		frame: 0,
	};

	for (let i = 0; i < frameCount; i++) {
		const img = new Image();
		img.src = imageFiles[i];
		img.style.width = "100%";
		img.style.height = "100%";
		images.push(img);
	}

	gsap.to(imageSequence, {
		frame: frameCount,
		snap: "frame",
		ease: "none",
		scrollTrigger: {
			trigger: canvas,
			start: "top top",
			end: "bottom top",
			scroller: "#main",
			scrub: 5,
			pin: canvas,
		},
		onUpdate: render,
	});

	images[0].onload = render;

	function render() {
		scaleImage(images[imageSequence.frame], context);
	}

	function scaleImage(image, context) {
		let canvas = context.canvas;
		let horizontalRatio = canvas.width / image.width;
		let verticalRatio = canvas.height / image.height;
		let ratio = Math.max(horizontalRatio, verticalRatio);
		let centerShift_x = (canvas.width - image.width * ratio) / 2;
		let centerShift_y = (canvas.height - image.height * ratio) / 2;
		context.clearRect(0, 0, canvas.width, canvas.height);
		context.drawImage(
			image,
			0,
			0,
			image.width,
			image.height,
			centerShift_x,
			centerShift_y,
			image.width * ratio,
			image.height * ratio
		);
	}

	ScrollTrigger.create({
		trigger: canvas,
		scroller: "#main",
		start: "top top",
		end: "700% top",
	});
}
